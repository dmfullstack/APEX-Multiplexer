
public class DependencyOfInstruction {
	private String inst_Name;
	
	public String getInstName() {
		return inst_Name;
	}
	private String reg_Name;
	public String getRegName() {
		return reg_Name;
	}
	public void setInstName(String instructionName) {
		this.inst_Name = instructionName;
	}
	public void setRegName(String registerName) {
		this.reg_Name = registerName;
	}
	
	
}
